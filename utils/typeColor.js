export default function typeColor(type) {
  switch (type) {
    case 'bug':
      return '#92bc2c'
      break
    case 'dark':
      return '#595761'
      break
    case 'dragon':
      return '#0c69c8'
      break
    case 'electric':
      return '#f2d94e'
      break
    case 'fire':
      return '#fba54c'
      break
    case 'fairy':
      return '#ee90e6'
      break
    case 'fighting':
      return '#d3425f'
      break
    case 'flying':
      return '#a1bbec'
      break
    case 'ghost':
      return '#5f6dbc'
      break
    case 'grass':
      return '#5fbd58'
      break
    case 'ground':
      return '#da7c4d'
      break
    case 'ice':
      return '#75d0c1'
      break
    case 'normal':
      return '#a0a29f'
      break
    case 'poison':
      return '#b763cf'
      break
    case 'psychic':
      return '#fa8581'
      break
    case 'rock':
      return '#c9bb8a'
      break
    case 'steel':
      return '#5695a3'
      break
    case 'water':
      return '#539ddf'
      break
    default:
      return '#a0a29f'
      break
  }
}
